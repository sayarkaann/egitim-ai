/**
 * Set UI language, persist to localStorage, apply RTL for Arabic.
 * @param {string} lang - Language code: tr | en | de | fr | ar | es | ru
 */
function setLanguage(lang) {
  if (!TRANSLATIONS[lang]) lang = 'tr';
  localStorage.setItem('ui_lang', lang);
  document.documentElement.setAttribute('lang', lang);
  // RTL support for Arabic
  document.documentElement.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');
  applyTranslations(lang);
  // Dispatch event so other scripts can react
  document.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
}

/**
 * Apply translations to all elements with data-i18n / data-i18n-placeholder.
 * @param {string} lang
 */
function applyTranslations(lang) {
  const dict = TRANSLATIONS[lang] || TRANSLATIONS.tr;

  // Text content
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    const text = dict[key] ?? TRANSLATIONS.tr[key] ?? key;
    el.textContent = text;
  });

  // Placeholder attributes
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.dataset.i18nPlaceholder;
    const text = dict[key] ?? TRANSLATIONS.tr[key] ?? key;
    el.placeholder = text;
  });

  // Title attributes
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.dataset.i18nTitle;
    const text = dict[key] ?? TRANSLATIONS.tr[key] ?? key;
    el.title = text;
  });
}

/**
 * Initialize i18n on page load. Reads saved language from localStorage.
 * @returns {string} current language code
 */
function initI18n() {
  const saved = localStorage.getItem('ui_lang') || 'tr';
  setLanguage(saved);
  return saved;
}

/**
 * Get a single translation string.
 * @param {string} key
 * @param {string} [lang] - defaults to current saved lang
 * @returns {string}
 */
function t(key, lang) {
  const l = lang || localStorage.getItem('ui_lang') || 'tr';
  const dict = TRANSLATIONS[l] || TRANSLATIONS.tr;
  return dict[key] ?? TRANSLATIONS.tr[key] ?? key;
}
